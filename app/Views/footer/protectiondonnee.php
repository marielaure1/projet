<h3>2. NATURE DES DONNÉES PERSONNELLES COLLECTÉES</h3>
<p>Vos « données personnelles » font référence à toutes les informations vous concernant en tant que personne physique et qui permettent de vous identifier, directement ou indirectement. Les données personnelles que nous sommes susceptibles de collecter lorsque vous naviguez sur le site sont :

- des données nominatives de base, telles que vos noms et prénoms, vos photographies, votre date de naissance, votre adresse postale, votre adresse e-mail, vos numéros de téléphone(*),
- des données techniques de connexion et de navigation, telles que l’adresse IP de votre terminal, la date & l'heure de votre connexion, les pages visitées, les caractéristiques de votre navigateur (type, langue, etc.) et de votre terminal (type, résolution, etc.), via des cookies
- des données de localisation géographique (géolocalisation),
- des données de publication, telles que vos avis, commentaires, notes, photographies de produits achetés,
- des données de comportement, telles que vos historiques d’achat.

(*) : Vous pouvez refuser d’être appelé(e) sur votre numéro de téléphone dans le cadre de tout démarchage téléphonique par des tiers, en vous inscrivant sur Bloctel (site internet : bloctel.gouv.fr).

Cookies
Cet article est destiné à vous informer sur l’origine et l’usage des informations de navigation traitées à l’occasion de votre consultation et vos droits en la matière.

En effet, lors de la consultation de notre site, des informations relatives à votre navigation sont susceptibles d’être enregistrées dans des fichiers « cookies », installés sur votre terminal. Ces cookies ne permettent pas de vous identifier mais enregistrent des informations relatives à la navigation de votre terminal sur notre site (les pages que vous avez consultées, la date et l'heure de la consultation, etc.), permettant une lecture de ces dernières lors de vos visites ultérieures facilitant vos navigations ultérieures sur notre site.

Ces cookies sont déposés sur notre site, soit directement par nous soit par certains de nos partenaires.

Decoclico vous informe qu’aucun cookie destiné à DECOCLICO ne sera émis via les sites de nos partenaires.

La durée de conservation de ces informations par DECOCLICO dans votre ordinateur est de 13 mois.

Nous vous informons néanmoins que si vous partagez l’utilisation de votre terminal avec d’autres personnes, nous ne pouvons pas nous assurer de manière certaine que les services et publicités destinés à votre terminal correspondent bien à votre propre utilisation de ce terminal et non à celle d’un autre utilisateur de ce terminal.

Les cookies que nous émettons via notre site :

Les cookies que nous émettons dans votre terminal nous permettent de reconnaître votre navigateur lorsque vous vous connectez à notre site. Ces derniers nous permettent de :

- D’établir des statistiques de fréquentation (nombre de visite, de pages vues, votre parcours,…), nous permettant d’améliorer de manière continue nos services

- D’adapter la présentation de notre site aux préférences d’affichage de votre terminal

- De mémoriser des informations saisies dans divers formulaires, (identification, contenu de votre panier,…).

- De gérer et d’accéder de manière sécurisée à vos espaces personnels (compte client, historiques de vos commandes,…)

- De fournir du contenu, y compris de la publicité, en rapport avec vos centres d’intérêts

Les cookies émis par des tiers via notre site :

Lors de la navigation sur notre site, il est possible qu’un ou plusieurs cookies de sociétés partenaires soient placés dans votre terminal via des pages de notre site. DECOCLICO ne dispose pas d’accès et de moyen de contrôles de ces derniers, qui ressortent de la responsabilité des tiers.

Ces cookies déposés par des tiers leur permettent d’identifier les produits consultés ou achetés sur notre site afin de pouvoir vous adresser des offres publicitaires correspondant à vos centres d’intérêts.

Vos choix quant aux cookies :

L’enregistrement d’un cookie reste subordonné à votre volonté. Vous disposez donc de la possibilité de paramétrer la typologie de cookies (cookies de personnalisation et cookies de publicité) qui seront déposés sur votre navigateur dès votre arrivée sur notre site. Vous pourrez également configurer votre logiciel de navigation de sorte à pouvoir vous opposer, totalement ou partiellement (notamment en fonction de l’émetteur) à l'enregistrement de "cookies". La configuration vous offre également la possibilité d’accepter ou refuser des cookies de manière ponctuelle, avant l’enregistrement de ce dernier dans votre terminal.

Aussi, au vu des finalités des cookies telles qu’exposées précédemment, tout paramétrage visant à refuser l’enregistrement de cookies sera susceptible de modifier votre navigation et/ou les fonctionnalités et service et conditions d’accès à certains services nécessitant l’utilisant de cookies, tels que par exemple une mise en page ergonomique et adaptée de notre site à votre terminal, le stockage de vos produits dans votre panier,…

Vous pouvez ainsi faire le choix d’exprimer à tout moment et de modifier vos souhaits en matière de cookies, en fonctionnant de la manière suivante :

- Pour Mozilla firefox : Choisissez le menu "outil " puis "Options", Cliquez sur l'icône "vie privée", Repérez le menu "cookie" et sélectionnez les options qui vous conviennent.

- Pour Microsoft Internet Explorer 6.0 et au delà : Choisissez le menu "Outils" (ou "Tools"), puis "Options Internet" (ou "Internet Options"), Cliquez sur l'onglet "Confidentialité" (ou "Confidentiality"), Sélectionnez le niveau souhaité à l'aide du curseur.

- Pour Microsoft Internet Explorer 5 : Choisissez le menu "Outils" (ou "Tools"), puis "Options Internet" (ou "Internet Options"), Cliquez sur l'onglet "Sécurité" (ou "Security"), Sélectionnez "Internet" puis "Personnaliser le niveau" (ou "CustomLevel"), Repérez la rubrique "cookies" et choisissez l'option qui vous convient.

- Pour Opéra 6.0 et au-delà : Choisissez le menu "Fichier">"Préférences", Vie Privée et sélectionnez les options qui vous conviennent.

- Pour Safari : Dans le menu de Safari choisir "Préférences" > "Sécurité" et sélectionnez les options qui vous conviennent.</p>

